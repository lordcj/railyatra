// Get train by number
export function getTrainByNumber(number: string): Train | undefined {
    return allTrains.find((train) => train.number === number);
}

// Get all train numbers
export function getAllTrainNumbers(): string[] {
    return allTrains.map((train) => train.number);
}

// Get trains by type
export function getTrainsByType(type: Train['type']): Train[] {
    return allTrains.filter((train) => train.type === type);
}

// Get trains from station
export function getTrainsFromStation(stationCode: string): Train[] {
    return allTrains.filter(
        (train) => train.sourceCode === stationCode || train.destinationCode === stationCode
    );
}

// Get popular trains (for sitemap priority)
export function getPopularTrains(limit = 100): Train[] {
    const typeOrder: Train['type'][] = ['Vande Bharat', 'Rajdhani', 'Shatabdi', 'Duronto', 'Superfast', 'Express', 'Mail'];

    return [...allTrains]
        .sort((a, b) => typeOrder.indexOf(a.type) - typeOrder.indexOf(b.type))
        .slice(0, limit);
}
